#!/bin/bash
# اسکریپت bash برای تولید کلید رمزنگاری

echo "============================================================"
echo "تولید کلید رمزنگاری برای ENCRYPTION_KEY"
echo "============================================================"
echo ""

# تولید کلید 32 بایتی و تبدیل به base64
KEY=$(openssl rand -base64 32)

echo "✅ کلید تولید شد!"
echo ""
echo "📋 برای استفاده در .env یا environment variable:"
echo "ENCRYPTION_KEY=$KEY"
echo ""
echo "⚠️  نکات مهم:"
echo "1. این کلید را در جای امن نگه دارید"
echo "2. هرگز در Git commit نکنید"
echo "3. اگر کلید را گم کنید، داده‌های رمزنگاری شده قابل بازیابی نیستند"
echo "4. در production حتماً از environment variable استفاده کنید"
echo ""
echo "============================================================"


